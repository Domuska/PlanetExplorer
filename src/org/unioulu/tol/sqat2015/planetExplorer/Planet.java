package org.unioulu.tol.sqat2015.planetExplorer;

public class Planet {

	int x;
	int y;
	
	/**
	 * 
	 * @param x plane size of the new planet
	 * @param y plane size of the new planet
	 */
	public Planet(int x, int y){
		
		this.x = x;
		this.y = y;
	}


	public int getX() {
		return x;
	}


	public int getY() {
		return y;
	}
	
	
	
}
